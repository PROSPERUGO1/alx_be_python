# A python scrip that calculates the simple interest earned on an investment over a period of time. The formula for simple interest is (I = P * R * T) , where:

principle = 1000 # initial amount of $1000
rate = 0.05 # 5% annual interest rate
time = 3 # 3 years

# Calculate the simple interest using the formular
interest = principle * rate * time
print(f"The simple interest is: {interest}")
